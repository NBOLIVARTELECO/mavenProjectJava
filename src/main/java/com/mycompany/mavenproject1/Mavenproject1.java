package com.mycompany.mavenproject1; // Declaración del paquete donde se encuentra la clase

/**
 * Clase principal de ejemplo para un proyecto Maven en Java.
 */
public class Mavenproject1 { // Declaración de la clase principal

    public static void main(String[] args) { // Método principal, punto de entrada del programa
        System.out.println("Hello World!"); // Imprime "Hello World!" en la consola
        System.out.println("segunda palabra!"); // Imprime "Hello World!" en la consola
        
    }

    /**
     * Método que suma dos números enteros.
     * @param a primer número
     * @param b segundo número
     * @return la suma de a y b
     */
    public static int sumar(int a, int b) { // Método estático que recibe dos enteros y retorna su suma
        return a + b; // Retorna la suma de los parámetros
    }
}
